#List with modes of transport

transportation = ["sports car", "yacht", "monster truck", "Buggy"]



#for loop to iterate over each item with a personalized statement

for x in range(len(transportation)):
    print(f"I would like to ride a {transportation[x]} at least once in my life")
    print(f"Having a {transportation[x]} would be amazing.")
    print(f"A {transportation[x]} is expensive even if its on rent!")

